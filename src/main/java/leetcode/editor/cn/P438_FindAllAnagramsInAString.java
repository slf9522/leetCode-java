    class Solution {
        public List<Integer> findAnagrams(String s, String p) {
            int[] all = new int[26];
            for (int i = 0; i < p.length(); i++) {
                all[p.charAt(i) - 'a'] += 1;
            }
            int i = 0, j = 0;
            int l = s.length();
            int[] sa = new int[26];
            List<Integer> res = new ArrayList<>();
            while (j < l) {
                int c = s.charAt(j) - 'a';
                sa[c] += 1;
                boolean compare = equal(all, sa);
                if (compare) {
                    res.add(i);
                    int ci = s.charAt(i) - 'a';
                    sa[ci] -= 1;
                    i++;
                    j++;
                } else if (i > j + 1 - p.length()) {
                    j++;
                } else {
                    int ci = s.charAt(i) - 'a';
                    sa[ci] -= 1;
                    i++;
                    j++;
                }
            }
            return res;
        }

        public boolean equal(int[] p, int[] w) {
            for (int i = 0; i < p.length; i++) {
                if (p[i] != w[i]) return false;
            }
            return true;
        }
    }
